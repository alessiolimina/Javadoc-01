package it.develhope.javadoc;

/**
 * This class represents Word Games
 * @author alessiolimina
 */
public class WordGames {

    /**
     * This method prints a word
     * @param word is the word that we'll use
     * @return is the return that we'll print
     */

    public String addHelloWorld(String word){
        return "Hello " + word;
    }

    /**
     * This method gets the full name
     * @param name is the name that we'll use
     * @param surname is the surname that we'll use
     * @return is the return that we'll print
     */

    public String getFullName(String name, String surname) {
        return name + " " + surname;


    }
}

